-- ------------------------------------------------
-- 1. questions - 测试题目表
-- ------------------------------------------------
DROP TABLE IF EXISTS `questions`;
CREATE TABLE `questions` (
    `id` INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    `dimension` ENUM('EI','SN','TF','JP') NOT NULL COMMENT '维度：EI=外向/内向, SN=感觉/直觉, TF=思维/情感, JP=判断/感知',
    `question_text` VARCHAR(500) NOT NULL COMMENT '题目内容',
    `option_a` VARCHAR(300) NOT NULL COMMENT '选项A（对应维度前字母：E/S/T/J）',
    `option_b` VARCHAR(300) NOT NULL COMMENT '选项B（对应维度后字母：I/N/F/P）',
    `sort_order` TINYINT UNSIGNED NOT NULL DEFAULT 0 COMMENT '同维度内排序',
    `is_active` TINYINT(1) NOT NULL DEFAULT 1 COMMENT '是否启用：1=启用, 0=禁用',
    `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='MBTI测试题目表';


-- ------------------------------------------------
-- 2. mbti_types - MBTI类型描述表
-- ------------------------------------------------
DROP TABLE IF EXISTS `mbti_types`;
CREATE TABLE `mbti_types` (
    `type_code` VARCHAR(4) PRIMARY KEY COMMENT 'MBTI类型代码，如INTJ',
    `type_name` VARCHAR(100) NOT NULL COMMENT '中文名称',
    `type_nickname` VARCHAR(100) NOT NULL COMMENT '别称/昵称',
    `type_color` VARCHAR(7) NOT NULL DEFAULT '#6C63FF' COMMENT '代表色(十六进制)',
    `description` TEXT NOT NULL COMMENT '类型概述',
    `strengths` TEXT COMMENT '优势特点（JSON数组）',
    `weaknesses` TEXT COMMENT '劣势特点（JSON数组）',
    `careers` TEXT COMMENT '适合职业（JSON数组）',
    `celebrities` TEXT COMMENT '代表人物（JSON数组）',
    `icon` VARCHAR(50) NOT NULL DEFAULT '🧠' COMMENT '代表图标'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='MBTI类型描述表';


-- ------------------------------------------------
-- 3. certificates - 测试结果/证书表
-- ------------------------------------------------
DROP TABLE IF EXISTS `certificates`;
CREATE TABLE `certificates` (
    `id` BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    `certificate_no` VARCHAR(20) NOT NULL UNIQUE COMMENT '证书编号，格式：MBTI-YYYYMMDD-XXXXXX',
    `name` VARCHAR(100) NOT NULL COMMENT '测试者姓名',
    `email` VARCHAR(200) DEFAULT NULL COMMENT '邮箱（选填）',
    `mbti_type` VARCHAR(4) NOT NULL COMMENT 'MBTI类型结果',
    `scores` JSON NOT NULL COMMENT '各维度得分 {"E":8,"I":12,...}',
    `result_detail` JSON DEFAULT NULL COMMENT '详细结果（JSON）',
    `ip_address` VARCHAR(45) DEFAULT NULL COMMENT '测试IP地址',
    `user_agent` VARCHAR(500) DEFAULT NULL COMMENT '用户浏览器信息',
    `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '测试时间',
    INDEX `idx_certificate_no` (`certificate_no`),
    INDEX `idx_mbti_type` (`mbti_type`),
    INDEX `idx_name` (`name`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='测试结果证书表';


-- ------------------------------------------------
-- 4. statistics - 每日类型统计表（可选）
-- ------------------------------------------------
DROP TABLE IF EXISTS `statistics`;
CREATE TABLE `statistics` (
    `id` INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    `stat_date` DATE NOT NULL COMMENT '统计日期',
    `mbti_type` VARCHAR(4) NOT NULL COMMENT 'MBTI类型',
    `count` INT UNSIGNED NOT NULL DEFAULT 0 COMMENT '测试人数',
    UNIQUE KEY `uk_date_type` (`stat_date`, `mbti_type`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='每日类型统计表';


